class User {
  final String imagePath;
  final String name;
  final String email;
  final String points;
  final String calories;
  final String hours;
  final String steps;
  final String date;
  final String last_exercise;

  const User({
    required this.imagePath,
    required this.name,
    required this.email,
    required this.points,
    required this.calories,
    required this.hours,
    required this.steps,
    required this.date,
    required this.last_exercise,
  });
}
